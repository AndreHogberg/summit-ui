namespace SummitUI;

/// <summary>
/// Represents the checked state of a checkbox.
/// </summary>
public enum CheckedState
{
    /// <summary>
    /// The checkbox is unchecked.
    /// </summary>
    Unchecked,

    /// <summary>
    /// The checkbox is in an indeterminate state (typically used for "select all" scenarios).
    /// </summary>
    Indeterminate,

    /// <summary>
    /// The checkbox is checked.
    /// </summary>
    Checked
}
